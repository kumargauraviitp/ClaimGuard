const iso = (d: string) => new Date(d).toISOString();

function shap(
  feature: string,
  label: string,
  contribution: number,
  value: string,
  category: ShapFactor["category"],
  description?: string,
): ShapFactor {
  const info = humanizeFeature(feature);
  return {
    feature,
    label,
    description: description || info.description,
    contribution,
    direction: contribution >= 0 ? "increase" : "decrease",
    value,
    category,
  };
}

